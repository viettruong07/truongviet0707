package Session10;

public class Course {
    private String title;
    private long score;
    private int id;
    public String getTitle(){
        return title;
    }
    public void setTitle(String title){
        this.title = title;
    }
    public long getScore(){
        return score;
    }
    public void setScore(long score){
        this.score = score;
    }
    public int getId(){
        return id;
    }
    public void setId(int id){
        this.id = id;
    }
}


