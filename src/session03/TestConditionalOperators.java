package session03;

public class TestConditionalOperators {
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 20;
        System.out.print("value1 == value2: ");
        System.out.println(value1 == value2);
        System.out.print("value1 != value2: ");
        System.out.println(value1 != value2);
        System.out.print("value1 > value2:");
        System.out.println(value1 > value2);
        System.out.print("value1 < value2: ");
        System.out.println(value1 < value2);
        System.out.print("value1 <= value2: ");
        System.out.println(value1 <= value2);
    }
}
